# Integer
num = 10

# Float
pi = 3.14159

# String
name = "John Doe"

# Boolean
is_true = True

# List
fruits = ['apple', 'banana', 'orange']

# Tuple
coordinates = (50, 60)

# Dictionary
person = {'name': 'Alice', 'age': 25, 'city': 'New York'}

# Set
numbers = {1, 2, 3, 4, 5}

# Printing the types and values
print("Type of num:", type(num), ", value:", num)
print("Type of pi:", type(pi), ", value:", pi)
print("Type of name:", type(name), ", value:", name)
print("Type of is_true:", type(is_true), ", value:", is_true)
print("Type of fruits:", type(fruits), ", value:", fruits)
print("Type of coordinates:", type(coordinates), ", value:", coordinates)
print("Type of person:", type(person), ", value:", person)
print("Type of numbers:", type(numbers), ", value:", numbers)
